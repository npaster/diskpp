#pragma once

class mapped_file
{
    const char *    m_start;
    const char *    m_end;
    std::string     m_name;
    int             m_fd;
    size_t          m_length;
    const char *    m_addr;
    bool            m_is_open;

    bool            map(const std::string&);
    bool            unmap(void);

public:
                    mapped_file();
                    mapped_file(const std::string& name);
                    ~mapped_file();

    void            open(const std::string& name);
    void            close();
    bool            is_open(void) const;

    bool            end(void) const;
    std::string     get_line(void);
    const char *    mem(void);
};
